    public String getStringFont(Font f) {
        String style = "Bold Italic";

        switch (f.getStyle()) {
            case Font.ITALIC:
                style = "Italic";
                break;
            case Font.BOLD:
                style = "Bold";
                break;
            case 3:
                style = "Bold Italic";
                break;
            case Font.PLAIN:
                style = "Plain";
                break;
            default:
                style = "Plain";
                break;
        }

        return f.getFamily() + "," + style + "," + f.getSize();
    }
